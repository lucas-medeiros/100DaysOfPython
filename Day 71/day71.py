@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        user = db.session.execute(db.select(User).where(User.email == form.email.data)).scalar()
        if user:
            flash("You've already signed up with that email, log in instead!")
            return redirect(url_for("login"))

        password = generate_password_hash(form.password.data, method="pbkdf2:sha256", salt_length=8)
        new_user = User(
            name=form.name.data,
            email=form.email.data,
            password=password
        )
        db.session.add(new_user)
        db.session.commit()

        # Log in and authenticate user after adding details to database.
        login_user(new_user)
        return redirect(url_for("get_all_posts"))

    return render_template("register.html", form=form, current_user=current_user)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        email = form.email.data
        password = form.password.data

        # Find user by email
        user = db.session.execute(db.select(User).where(User.email == email)).scalar()

        # Check if user exists and stored password hash against entered password hashed.
        if user and check_password_hash(pwhash=user.password, password=password):
            # Log in and authenticate user
            login_user(user)
            return redirect(url_for('get_all_posts'))
        else:
            flash('Email or password incorrect, please try again.')
            return render_template('login.html', current_user=current_user), 401
    return render_template('login.html', current_user=current_user)


